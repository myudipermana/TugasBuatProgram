    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        String id = jLabel9.getText();
        int harga = Integer.parseInt(jLabel7.getText());
        int uang = Integer.parseInt(jTextField1.getText());
        int proses = harga - uang;
        
        String ket = jLabel4.getText();
        
        if(proses>0)
        {
            ket="Belum Bisa Beli";
            jLabel4.setText(ket);
        }
       
        if (proses<=0)
        {
            ket="Beli dong";
            jLabel4.setText(ket);
        }
        
        
        try {
            koneksi.koneksiDatabase();
            String kueriEdit = "update tb_whis set uang='"+uang+"',ket='"+ket+"'where id ='"+id+"'";
            koneksi.state.executeUpdate(kueriEdit);
            dataTable();
            JOptionPane.showMessageDialog(null, "Data Berhasil di ubah");
            
        } catch (SQLException ex) {
            Logger.getLogger(Tampil.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_jButton1ActionPerformed

