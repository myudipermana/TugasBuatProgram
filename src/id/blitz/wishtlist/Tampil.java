    private void dataTable(){
        DefaultTableModel tabel = new DefaultTableModel();
        
        tabel.addColumn("No");
        tabel.addColumn("Tanggal");
        tabel.addColumn("Bulan");
        tabel.addColumn("Nama Barang");
        tabel.addColumn("Harga Barang");
        tabel.addColumn("Uang Terkumpul");
        tabel.addColumn("Keterangan");
        
        jTable1.setModel(tabel);
        
        try {
            String sql = "select * from tb_whis";
            
            ResultSet res = koneksi.state.executeQuery(sql);
            while(res.next()){
                tabel.addRow(new Object[]{
                    res.getString("id"),
                    res.getString("tgl"),
                    res.getString("bulan"),
                    res.getString("brg"),
                    res.getString("harga"),
                    res.getString("uang"),
                    res.getString("ket")
                });
                jTable1.setModel(tabel);
                
            }
        } catch (SQLException eek) {
            System.err.println("SQLException:"+eek.getMessage());
        }
         
        }
